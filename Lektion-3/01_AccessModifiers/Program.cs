/*  
    Access Modifiers (Encapsulation - OOP)
    --------------------------------------------------------------------------------
    public          accessbar överallt, dvs i alla projekt, i alla relationer, alla assemblies      
    internal        accessbar inom det egna projektet (assembly)
    protected       accessbar bara genom arv av klasser    
    private         bara accessbar inom sitt egna kodblock {} 

    (en klass kan bara vara public eller internal)

    Special fall:
        protected internal
        private protected
*/
